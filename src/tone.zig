pub const ToneMark = enum {
    // Dấu sắc (acute accent) - rising tone
    Acute,
    // Dấu huyền (grave accent) - falling tone
    Grave,
    // Dấu hỏi (hook above) - dipping tone
    HookAbove,
    // Dấu ngã (tilde) - creaky rising tone
    Tilde,
    // Dấu nặng (dot below) - creaky falling tone
    Underdot,
};
