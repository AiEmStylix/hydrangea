pub fn getAccute(c: u21) ?u21 {
    return switch (c) {
        'a' => 'á',
        'â' => 'ấ',
        'ă' => 'ắ',
        'e' => 'é',
        'ê' => 'ế',
        'i' => 'í',
        'o' => 'ó',
        'ô' => 'ố',
        'ơ' => 'ớ',
        'u' => 'ú',
        'ư' => 'ứ',
        'y' => 'ý',
        // UPPERCASE
        'A' => 'Á',
        'Â' => 'Ấ',
        'Ă' => 'Ắ',
        'E' => 'É',
        'Ê' => 'Ế',
        'I' => 'Í',
        'O' => 'Ó',
        'Ô' => 'Ố',
        'Ơ' => 'Ớ',
        'U' => 'Ú',
        'Ư' => 'Ứ',
        'Y' => 'Ý',
        else => unreachable,
    };
}

